public class Subject{
    private final String name;
    private final char[] sequence;
    
    public Subject(String name, char[] sequence)
    {
        this.name = name;
        this.sequence =  sequence;
    }
    
    public String getName()
    {
        return name;
    }
    
    public char[] getSequence()
    {
        return sequence;
    }
    
 
    

    
}